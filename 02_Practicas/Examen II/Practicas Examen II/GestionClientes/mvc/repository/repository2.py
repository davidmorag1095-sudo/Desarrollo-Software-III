import os
import json

class Repository2:
    def __init__(self, from_dict, nombre_archivo):
        self.list = []
        self.from_dict = from_dict

        dir_data = os.path.dirname(__file__)
        self.archivo = os.path.join(dir_data, 'datos', nombre_archivo)
        self.load()
#------------------------------------------------------------------------------------------------------------
    def load(self):
        with open(self.archivo, 'r', encoding = 'utf-8') as file:
            data = json.load(file)

        for items in data:
            if items:
                self.list.append(self.from_dict(items))


